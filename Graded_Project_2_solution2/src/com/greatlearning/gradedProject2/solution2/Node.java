//Java implementation to flatten the
//binary search tree into a skewed
//tree in increasing / decreasing order


//Class of the node
 public class Node
{
	int val;
	Node left, right;
	
	// Helper function that allocates a new node
	// with the given data and NULL left and right
	// pointers.
	Node(int item)
	{
		val = item;
		left = right = null;
	}
}
 


